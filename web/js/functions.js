function refresh_page() {
	if (new Date().getTime() - time_reload_page >= ReloadPageInterval){
		$.ajax({
		     url:  '/?action=clearCart',
		     type: 'GET',
		     success: function(html){
		    	 window.location.reload(true);
		     },
		     error: function(){
		       alert("error");
		     }  
		  }); 
		
	}
	else
		setTimeout(refresh_page, 30000);
}

