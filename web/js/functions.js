function remove_vote(productId){
	if(productId)
		$.ajax({
		     url:  '/?action=removeVote&productId='+productId,
		     type: 'GET',
		     success: function(html){
		    	 $('.vote_item_'+productId).remove()
		     },
		     error: function(){
		       alert("error");
		     }  
		  }); 
	
}
